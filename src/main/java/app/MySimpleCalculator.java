package app;

public class MySimpleCalculator {

	public double add2Num(double num1, double num2) {
		
		return num1+num2;
	}

	public double sub2Num(double num1, double num2) {
		
		return num1-num2;
	}
	
	public double mul2Num(double num1, double num2) {
		
		return num1*num2;
	}
	
	public double div2Num(double num1, double num2) {
		
		return num1/num2;
	}
	
}
